    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
          validateInputs();
        if(validateInputs()==false)
        { 
       JOptionPane.showMessageDialog(null,"Please Input All Fields:","ERROR!",JOptionPane.ERROR_MESSAGE);
       return;
        }
        
            String optionName=options.get(correctOption);
            Questions obj=new Questions(exam.getExamId(),qno,exam.getLanguage(),option1,option2,option3,option4,optionName,question);
            qstore.addQuestion(obj);
            clearAll();
            lblTitle.setText(exam.getTotal()-qno+" Questions Remaining");
            qno++;
            if(qno>exam.getTotal())
            {   disableAll();
            JOptionPane.showMessageDialog(null,"Your Question Set has been created Succesfully.\n Press the DONE button to add in the database","Exam Added",JOptionPane.INFORMATION_MESSAGE);
            }
            else
           lblQno.setText("Question no: "+qno);     
            
    }//GEN-LAST:event_btnNextActionPerformed

