    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        // TODO add your handling code here:
        if(qno<=exam.getTotal())
        {
            int questionremaining=exam.getTotal()-qno+1;
            JOptionPane.showMessageDialog(null,"Number Of Question"+questionremaining+" Remaining","Cannot add Exam!",JOptionPane.ERROR_MESSAGE);
            return;
            }
        try
        {
            ExamDAO.getExam(exam);
            QuestionDAO.addQuestions(qstore);
            JOptionPane.showMessageDialog(null,"Your Question Set has been created Succesfully","Exam Added",JOptionPane.INFORMATION_MESSAGE);
            adminOptionsFrame adminFrame=new adminOptionsFrame();
            adminFrame.setVisible(true);
            this.dispose();
        }
       catch(SQLException sq)
                    {
                    JOptionPane.showMessageDialog(null,"SQL Exception"+sq,"ERROR!",JOptionPane.ERROR_MESSAGE);
                    
                    } 
            
       
    }//GEN-LAST:event_btnDoneActionPerformed

